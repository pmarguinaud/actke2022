END SUBROUTINE
INTEGER*8 FUNCTION SIZE_TMSE (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TMSE),     INTENT (IN) :: YD
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
LOGICAL,          INTENT (IN) :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE

KSIZE = 0
ISIZE = KIND (YD%LPGDFWR)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%LPGDFWR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LHISFWR)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%LHISFWR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LFTZERO)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%LFTZERO'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NSURFEXCTLMAX)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%NSURFEXCTLMAX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NSURFEXCTL)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%NSURFEXCTL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CPREFIX_SFX1) * LEN (YD%CPREFIX_SFX1)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%CPREFIX_SFX1'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CPREFIX_SFX2) * LEN (YD%CPREFIX_SFX2)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%CPREFIX_SFX2'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%XZSEPS)
IF (LDPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CDPATH)//'%XZSEPS'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

END MODULE
